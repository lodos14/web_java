
public class IfApp {

	public static void main(String[] args) {
		
		String mode = "On";
		
		System.out.println("a");
		
		if(mode == "On") {
			System.out.println(1);
		} else if (mode == "Off"){
			System.out.println(2);
		} else {
			System.out.println(3);			
		}
		
		System.out.println("b");
	}

}
